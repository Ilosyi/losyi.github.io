//输入一段正文，统计字符数和行数
#include <stdio.h>
int main()
{
	char c = 0;
	int numchar = 0, numline = 0;
	printf("输入若干行文本，行首输入Ctrl+z结束。\n");
	while ((c = getchar()) != EOF)//注意括号
	{
		numchar++;
		if (c == '\n')
			numline++;
	}
	printf("字符数：%d\n", numchar);
	printf("行数：%d\n", numline);

	return 0;
}
